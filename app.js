function escapeHtml(str) {
  return String(str || '')
    .replaceAll('&', '&amp;')
    .replaceAll('<', '&lt;')
    .replaceAll('>', '&gt;')
    .replaceAll('"', '&quot;')
    .replaceAll("'", '&#39;');
}

function buildPacks() {
  return PACKS_META.map(meta => {
    const items = lessons.filter(lesson => meta.categories.includes(lesson.category));
    const completed = items.filter(item => state.completed.includes(item.id)).length;
    return { ...meta, lessons: items, completed };
  });
}

function getPacks() {
  const packs = buildPacks();
  packMap.clear();
  packs.forEach(pack => packMap.set(pack.id, pack));
  return packs;
}

function getPackForLesson(lessonId) {
  const packs = getPacks();
  return packs.find(pack => pack.lessons.some(lesson => lesson.id === lessonId)) || null;
}

