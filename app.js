function formatLessonBody(body) {
  const clean = String(body || '').trim();
  if (!clean) return '<p class="muted">ยังไม่มีเนื้อหา</p>';
  const blocks = clean.split(/\n\n+/).map(block => block.trim()).filter(Boolean);
  return blocks.map((block, blockIndex) => {
    const lines = block.split('\n').map(line => line.trim()).filter(Boolean);
    const isList = lines.length > 1 && lines.every(line => /^([0-9]+[.)]|[-•])\s+/.test(line));
    if (isList) {
      return `<ul>${lines.map(line => `<li>${escapeHtml(line.replace(/^([0-9]+[.)]|[-•])\s+/, ''))}</li>`).join('')}</ul>`;
    }
    if (lines.length === 1 && blockIndex === 0 && lines[0].length < 80) {
      return `<p class="body-title">${escapeHtml(lines[0])}</p>`;
    }
    return `<p>${escapeHtml(block).replace(/\n/g, '<br>')}</p>`;
  }).join('');
}

function renderReader() {
  const packs = getPacks();
  const pack = packs.find(item => item.id === state.currentPackId) || packs[0] || null;
  if (!pack) return;
  if (!state.currentPackId) state.currentPackId = pack.id;
  if (!state.currentLessonId || !pack.lessons.some(lesson => lesson.id === state.currentLessonId)) {
    state.currentLessonId = pack.lessons[0]?.id || null;
  }
  const lesson = lessonMap.get(state.currentLessonId);
  const completedInPack = pack.lessons.filter(item => state.completed.includes(item.id)).length;
  const pct = Math.round((completedInPack / (pack.lessons.length || 1)) * 100);
  byId('readerPackBadge').textContent = pack.badge;
  byId('readerPackTitle').textContent = pack.title;
  byId('readerPackDesc').textContent = pack.description;
  byId('readerPackLessonCount').textContent = pack.lessons.length;
  byId('readerPackCompleteCount').textContent = completedInPack;
  byId('readerPackProgress').style.width = `${pct}%`;
  byId('readerPackProgressText').textContent = `${pct}% (${completedInPack}/${pack.lessons.length} บท)`;
  byId('readerLessonList').innerHTML = pack.lessons.map((item, idx) => {
    const done = state.completed.includes(item.id);
    const fav = state.favorites.includes(item.id);
    return `
      <button class="lesson-nav-item ${item.id === state.currentLessonId ? 'active' : ''}" data-lesson-id="${item.id}">
        <span class="idx">บทที่ ${idx + 1} • ${item.category}${done ? ' • อ่านแล้ว' : ''}${fav ? ' • โปรด' : ''}</span>
        <strong>${item.title}</strong>
        <small>${item.summary}</small>
      </button>
    `;
  }).join('');
  if (!lesson) {
    byId('readerLessonTitle').textContent = 'ยังไม่มีบทเรียน';
    byId('readerLessonSummary').textContent = 'เลือกชุดการเรียนอื่น';
    byId('readerLessonBody').innerHTML = '<p class="muted">ยังไม่มีข้อมูล</p>';
    return;
  }
  byId('lessonMetaTags').innerHTML = `
    <span class="mini-tag">${pack.icon} ${pack.title}</span>
    <span class="mini-tag">${lesson.category}</span>
    <span class="mini-tag">ระดับ ${lesson.level}</span>
    ${state.completed.includes(lesson.id) ? '<span class="mini-tag">อ่านจบแล้ว</span>' : ''}
    ${state.favorites.includes(lesson.id) ? '<span class="mini-tag">อยู่ในรายการโปรด</span>' : ''}
  `;
  byId('readerLessonTitle').textContent = lesson.title;
  byId('readerLessonSummary').textContent = lesson.summary;
  byId('readerLessonBody').innerHTML = formatLessonBody(lesson.body);
  byId('favoriteLessonBtn').textContent = state.favorites.includes(lesson.id) ? 'เอาออกจากรายการโปรด' : 'บันทึกเป็นรายการโปรด';
  const idx = pack.lessons.findIndex(item => item.id === lesson.id);
  byId('prevLessonBtn').disabled = idx <= 0;
  byId('nextLessonBtn').disabled = idx >= pack.lessons.length - 1;
}

function openLessonInCurrentPack(lessonId) {
  state.currentLessonId = lessonId;
  renderReader();
  window.scrollTo({ top: 0, behavior: 'smooth' });
}

function moveLesson(step) {
  const pack = getPacks().find(item => item.id === state.currentPackId);
  if (!pack) return;
  const idx = pack.lessons.findIndex(item => item.id === state.currentLessonId);
  const next = pack.lessons[idx + step];
  if (next) openLessonInCurrentPack(next.id);
}

function toggleFavorite(id = state.currentLessonId) {
  if (!id) return;
  if (state.favorites.includes(id)) {
    state.favorites = state.favorites.filter(item => item !== id);
  } else {
    state.favorites.unshift(id);
  }
  persist();
}
window.toggleFavorite = toggleFavorite;

function completeCurrentLesson() {
  if (!state.currentLessonId) return alert('กรุณาเลือกบทเรียนก่อน');
  if (!state.completed.includes(state.currentLessonId)) state.completed.push(state.currentLessonId);
  persist();
  alert('บันทึกว่าอ่านจบแล้ว');
}

